CREATE TABLE tb_informaciones(
                            id_informacion           INT(11)   NOT NULL     AUTO_INCREMENT    PRIMARY KEY,
                            nombres_empresa                  VARCHAR(255)  NULL,
                            rif_empresa                     VARCHAR(255)  NULL,
                            direccion_empresa          VARCHAR(255)  NULL,
                            telefono_empresa             VARCHAR(255)  NULL,
                            estado                    VARCHAR(10)

);